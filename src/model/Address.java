package model;

/**
 *
 * @author gruber
 */
public class Address {
    
    private int id, number;
    private String street, city, state, cep;

    public Address() {
    }

    public Address(int id, int number, String street, String city, String state, String cep) {
        this.id = id;
        this.number = number;
        this.street = street;
        this.city = city;
        this.state = state;
        this.cep = cep;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }
    
}
